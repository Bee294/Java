public class Data_types {
    public static void main(String[] args) {
        int myNum = 5;//kieu du lieu uu tien khi tao bien co gia tri so
        long Num1 = 9999999999L;//kieu du lieu khi bien lon hon int, ket thuc bang gia tri L
        float myFloatNum = 5.99f;//kieu du lieu su dung khi bien co gia tri thap phan, ket thuc bang gia tri f
        double Num2 = 19.99d;//nhieu chu so thap phan hon floa, ket thuc bang gia tri d
        char myLetter = 'D';
        boolean myBool = true;
        String myText = "Hello";
        System.out.println(myNum);
        System.out.println(myFloatNum);
        System.out.println(myLetter);
        System.out.println(myBool);
        System.out.println(myText);
        System.out.println(Num1);
        System.out.println(Num2);


        float f1 = 35e3f;//chu e bieu thi luy thua cua 10
        double d1 = 12E4d;
        System.out.println(f1);
        System.out.println(d1);


        boolean isJavaFun = true;// kieu du lieu duoc khai bao voi boolean chi co the nhan gia tri true or false
        boolean isFishTasty = false;
        System.out.println(isJavaFun);
        System.out.println(isFishTasty);

        char myGrade = 'B';//Kieu du lieu char de luu tru 1 ki tu
        System.out.println(myGrade);

        String greeting = "Hello World";//Kieu du lieu String de luu tru mot chuoi ki tu(van ban)
        System.out.println(greeting);


    }
}
